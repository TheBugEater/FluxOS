void kernel_isr_handler(unsigned char interuptNumber, unsigned char errno)
{
    if(interuptNumber < 32)
    {
        printf("\nError Code : %d : %s", errno, exception_messages[interuptNumber]);
        for(;;);
    }
    else if(interuptNumber < 48)
    {
        if(interuptNumber > 40)
        {
            outb(0xA0, 0x20);
        }

        outb(0x20, 0x20);

        if(irq_procedures[interuptNumber - 32])
        {
            irq_procedures[interuptNumber - 32]();
        }
    }
}

